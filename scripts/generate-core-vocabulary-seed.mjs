import { build } from 'esbuild'
import { mkdir, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { pathToFileURL } from 'node:url'

const defaultOutputPath = '.wrangler/generated/core-vocabulary-seed.sql'
const outputArgIndex = process.argv.indexOf('--out')
const outputPath = resolve(
  outputArgIndex >= 0 ? process.argv[outputArgIndex + 1] : defaultOutputPath,
)
const compiledDataPath = resolve('.wrangler/generated/core-vocabulary-data.mjs')

await mkdir(dirname(outputPath), { recursive: true })
await mkdir(dirname(compiledDataPath), { recursive: true })

await build({
  entryPoints: ['src/data/vocabulary/core-3000.ts'],
  outfile: compiledDataPath,
  bundle: true,
  platform: 'node',
  format: 'esm',
  logLevel: 'silent',
})

const { coreVocabulary } = await import(
  `${pathToFileURL(compiledDataPath).href}?t=${Date.now()}`
)

const columns = [
  'id',
  'word',
  'normalized_word',
  'entry_type',
  'lemma',
  'meaning_zh',
  'definition_en',
  'primary_part_of_speech',
  'level',
  'frequency_rank',
  'frequency_band',
  'learning_priority',
  'publish_status',
  'note',
  'created_at',
  'updated_at',
  'reviewed_at',
]

const hasCjk = (value) => /[\u3400-\u9fff]/u.test(value)
const normalizeWord = (word) => word.trim().toLowerCase().replace(/\s+/g, ' ')
const getEntryType = (item) =>
  item.partOfSpeech === 'phrase' || /\s/.test(item.word) ? 'phrase' : 'word'
const getFrequencyBand = (rank) => {
  if (rank <= 100) return 'top-100'
  if (rank <= 500) return 'top-500'
  if (rank <= 1000) return 'top-1000'
  return 'top-3000'
}
const normalizeReviewedAt = (value) => {
  if (!value) return null
  return /^\d{4}-\d{2}-\d{2}$/.test(value) ? `${value}T00:00:00.000Z` : value
}
const sql = (value) => {
  if (value === null || value === undefined) return 'NULL'
  if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'NULL'
  return `'${String(value).replaceAll("'", "''")}'`
}

if (coreVocabulary.length !== 3000) {
  throw new Error(`Expected 3000 vocabulary entries, received ${coreVocabulary.length}`)
}

const now = new Date().toISOString()
const normalizedWords = new Set()
const rows = coreVocabulary.map((item) => {
  const normalizedWord = normalizeWord(item.word)
  if (normalizedWords.has(normalizedWord)) {
    throw new Error(`Duplicate normalized_word: ${normalizedWord}`)
  }
  normalizedWords.add(normalizedWord)

  const meaning = item.meaning ?? ''
  const meaningZh = hasCjk(meaning) ? meaning : ''
  const definitionEn = hasCjk(meaning) ? '' : meaning
  const frequencyRank = item.frequencyRank ?? item.priority
  const learningPriority = item.learningPriority ?? item.priority

  return {
    id: item.id,
    word: item.word,
    normalized_word: normalizedWord,
    entry_type: getEntryType(item),
    lemma: null,
    meaning_zh: meaningZh,
    definition_en: definitionEn,
    primary_part_of_speech: item.partOfSpeech,
    level: item.level,
    frequency_rank: frequencyRank,
    frequency_band: item.frequencyBand ?? getFrequencyBand(frequencyRank),
    learning_priority: learningPriority,
    publish_status: 'active',
    note: item.note ?? '',
    created_at: now,
    updated_at: now,
    reviewed_at: normalizeReviewedAt(item.quality?.lastReviewed),
  }
})

const chunk = (items, size) => {
  const chunks = []
  for (let index = 0; index < items.length; index += size) {
    chunks.push(items.slice(index, index + size))
  }
  return chunks
}

const updateAssignments = columns
  .filter((column) => column !== 'id' && column !== 'created_at')
  .map((column) => `${column} = excluded.${column}`)
  .join(',\n    ')

const insertStatements = chunk(rows, 100)
  .map((batch) => {
    const values = batch
      .map((row) =>
        `(${columns.map((column) => sql(row[column])).join(', ')})`,
      )
      .join(',\n  ')

    return `INSERT INTO core_vocabulary (${columns.join(', ')})\nVALUES\n  ${values}\nON CONFLICT(id) DO UPDATE SET\n    ${updateAssignments};`
  })
  .join('\n\n')

const sqlFile = `-- Generated by scripts/generate-core-vocabulary-seed.mjs.\n-- This file is intentionally written to .wrangler/generated by default and should not be committed.\n-- It upserts the current Core 3000 vocabulary into D1 without deleting existing rows.\n-- Do not wrap this file in BEGIN/COMMIT; remote D1 execute manages statement execution itself.\n\n${insertStatements}\n`

await writeFile(outputPath, sqlFile)
console.log(`Generated ${rows.length} core vocabulary rows at ${outputPath}`)
